"""
text.py — 리포트 텍스트 정제 [그룹 3 / LLM ✗]

LLM 출력에서 이모지·이모티콘·장식 기호를 결정론적으로 제거한다.
프롬프트 가드레일은 완벽하지 않고, 입력 데이터에도 이모지가 섞여 있으므로
'코드가 마지막에 보증'한다(숫자를 코드가 박는 것과 같은 원칙).
"""

import re

# 이모지/픽토그램/장식 기호 유니코드 블록
_EMOJI = re.compile(
    "["
    "\U0001F300-\U0001FAFF"   # 기호·픽토그램·보충(🤖 💡 🔍 등)
    "\U00002600-\U000027BF"   # 기타 기호·딩벳(⚠ ➔ ✔ 등)
    "\U00002300-\U000023FF"   # 기술 기호(⏱ 등)
    "\U00002B00-\U00002BFF"   # 화살표·별 기호
    "\U0001F1E6-\U0001F1FF"   # 지역 표시(국기)
    "\U0000FE00-\U0000FE0F"   # 변이 선택자(이모지 변형)
    "\U0000200D"              # ZWJ(이모지 결합)
    "\U00002190-\U00002191"   # 화살표 ← ↑ (장식용)
    "\U00002193-\U000021FF"   # 화살표 ↓ 등 (장식용). →(U+2192)는 표·경로 구분자라 보존
    "]+",
    flags=re.UNICODE,
)


def norm_for_match(s):
    """인용문 대조용 정규화 — 공백·줄바꿈을 전부 제거해 '내용만' 비교한다.
    LLM이 원문을 인용할 때 공백/개행을 다르게 옮기는 사소한 차이 때문에
    대조가 실패하지 않게 한다(verify quote / editorial fix_source 검증에 사용)."""
    if not isinstance(s, str):
        return ""
    return re.sub(r"\s+", "", s)


def strip_emojis(s):
    """문자열에서 이모지/장식 기호를 제거한다. 문자열이 아니면 그대로 반환."""
    if not isinstance(s, str):
        return s
    s = _EMOJI.sub("", s)
    # 제거 후 생긴 군더더기 공백 정리(줄바꿈은 보존)
    s = re.sub(r"[ \t]{2,}", " ", s)
    s = re.sub(r" *\n *", "\n", s)
    return s.strip()
